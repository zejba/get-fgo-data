async function sendData(form) {
  const FD = new FormData(form);
  const id = Number(FD.get('svtId'));
  if (!id) {
    alert('数字で入力してください');
    return;
  }
  const url = `https://api.atlasacademy.io/nice/JP/servant/${id}?lore=false`;
  try {
    const response = await fetch(url);
    if (!response.ok) {
      throw new Error(`レスポンスステータス: ${response.status}`);
    }
    const json = await response.json();
    const table = document.getElementById('resultTableBody');
    let newTable = buildTable(parseData(json));
    for (const np of json.noblePhantasms) {
      newTable += `<tr class="npName"><td>宝具</td><td>${np.name}</td></tr>${buildTable(parseNP2(np))}`;
    }
    for (const skill of json.skills) {
      newTable += `<tr class="skillName"><td>スキル${skill.num}${skill.priority === 1 ? '' : '(強化後)'}</td><td>${skill.name}</td></tr>${buildTable(parseSkill(skill))}`;
    }
    let i = 1;
    for (const skill of json.classPassive) {
      newTable += `<tr class="skillName"><td>クラススキル${i}</td><td>${skill.name}</td></tr>${buildTable(parseSkill(skill))}`;
      i++;
    }
    // for (const skill of json.extraPassive) {
    //     newTable += `<tr><td>クラススキル${i}</td><td>${skill.name}</td></tr>${buildTable(parseSkill(skill))}`
    //     i++
    // }
    table.innerHTML = newTable;
  } catch (error) {
    alert(error.message);
  }
}
function parseData(data) {
  const ret = {
    ID: data.collectionNo,
    名前: data.name,
    レアリティ: data.rarity,
    クラス: data.className,
    副属性: data.attribute,
    通常最大ATK: data.atkMax,
    聖杯最大ATK: data.atkGrowth[data.atkGrowth.length - 1],
    ...parseNP(data.noblePhantasms[data.noblePhantasms.length - 1], 0),
    'N/A': parseRate(data.noblePhantasms[data.noblePhantasms.length - 1].npGain.buster[0], 100),
    星: parseRate(data.starGen),
    Bhit: data.hitsDistribution.buster.length,
    Ahit: data.hitsDistribution.arts.length,
    Qhit: data.hitsDistribution.quick.length,
    Exhit: data.hitsDistribution.extra.length,
    Nhit: data.noblePhantasms[data.noblePhantasms.length - 1].npDistribution.length,
    cards: data.cards.map((v) => v[0].toUpperCase()).join('')
  };
  return ret;
}

function parseNP(data) {
  const damageVals = data.functions.find((func) => {
    return func.funcType.includes('damageNp');
  });
  const ret = {
    宝具色: data.card[0],
    宝具倍率: parseRate(damageVals?.svals[4]?.Value ?? 0)
  };
  return ret;
}

function parseNP2(data) {
  const damageVals = data.functions.find((func) => {
    return func.funcType.includes('damageNp');
  });
  const ret = {
    宝具色: data.card[0].toUpperCase(),
    詳細: data.detail,
    宝具倍率: `${parseRate(damageVals?.svals[4]?.Value ?? 0)}%${
      damageVals?.svals[4]?.Correction
        ? `（特攻倍率：${parseRate(damageVals?.svals[4]?.Correction)}%${
            damageVals?.svals[4]?.Correction !== damageVals?.svals[0]?.Correction
              ? '・宝具レベル依存'
              : damageVals?.svals[4]?.Correction !== damageVals?.svals5[4]?.Correction
                ? `・OC依存・最大${parseRate(damageVals?.svals5[4]?.Correction)}%`
                : '固定'
          }）`
        : ''
    }`,
    '効果倍率(宝具5/OC1)': data.functions
      .filter((func) => !func.funcType.includes('damageNp') && func.funcTargetTeam !== 'enemy')
      .map((func) => {
        const value = parseValue(func.svals[4]?.Value, func.funcType, func.buffs[0]?.type ?? '');
        const text = func.funcPopupText || (func.funcType === 'gainHp' ? 'HP回復' : '不明');
        return `${text}：${value}(確率${parseRate(func.svals[4]?.Rate) || '不明'}%)`;
      })
      .join('<br />')
  };
  return ret;
}

function parseSkill(data) {
  const ret = {
    詳細: data.detail,
    '効果倍率(Lv.10)': data.functions
      .filter((func) => func.funcTargetTeam !== 'enemy')
      .map((func) => {
        const value = parseValue(func.svals[9]?.Value, func.funcType, func.buffs[0]?.type ?? '');
        const text = func.funcPopupText || (func.funcType === 'gainHp' ? 'HP回復' : '不明');
        return `${text}：${value}（確率${parseRate(func.svals[9]?.Rate) || '不明'}%）`;
      })
      .join('<br />')
  };
  return ret;
}

function parseValue(value, funcType, buffType) {
  if (!value) {
    return '値無し';
  }
  const type = buffType || funcType;
  if (type.includes('gainStar')) {
    return `${value}個`;
  }
  if (type.includes('gainNp')) {
    return parseRate(value, 100);
  }
  if (type.includes('gainHp') || type.includes('Maxhp') || type.includes('lossHpSafe') || type.includes('reduceHp')) {
    return value;
  }
  return `${parseRate(value, 10)}%`;
}

function parseRate(x, bottom = 10) {
  const numX = Number(x);
  if (numX) {
    return numX / bottom;
  }
  return x;
}

function buildTable(data) {
  return Object.entries(data)
    .map(([k, v]) => {
      if (typeof v === 'string' || typeof v === 'number') {
        return `<tr><td>${k}</td><td>${v}</td></tr>`;
      }
    })
    .join('');
}

function copyTable() {
  const trs = document.getElementsByTagName('td');
  const ret = [];
  for (let i = 1; i <= 31; i += 2) {
    ret.push(trs[i].textContent);
  }
  navigator.clipboard.writeText(ret);
}
